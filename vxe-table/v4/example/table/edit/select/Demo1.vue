<template>
  <div>
    <vxe-table
      border
      show-overflow
      :column-config="{resizable: true}"
      :data="tableData"
      :edit-config="{trigger: 'click', mode: 'cell'}">
      <vxe-column type="seq" width="60"></vxe-column>
      <vxe-colgroup title="分组1">
        <vxe-column field="name" title="Name" :edit-render="{autofocus: '.my-input', autoselect: true}">
          <template #edit="{ row }">
            <input v-model="row.name" type="text" class="my-input">
          </template>
        </vxe-column>
        <vxe-column field="sex" title="Sex" :edit-render="{}">
          <template #default="{ row }">
            <span>{{ formatGroupSex(row.sex) }}</span>
          </template>
          <template #edit="{ row }">
          <vxe-select v-model="row.sex" type="text" :optionGroups="sexGriupList" transfer></vxe-select>
        </template>
        </vxe-column>
      </vxe-colgroup>
      <vxe-colgroup title="分组1">
        <vxe-column field="date13" title="Date" :edit-render="{autofocus: '.my-input .vxe-input--inner', autoselect: true}">
          <template #edit="{ row }">
            <vxe-input v-model="row.date13" type="date" class="my-input" transfer></vxe-input>
          </template>
        </vxe-column>
      </vxe-colgroup>
    </vxe-table>
  </div>
</template>

<script lang="ts" setup>
import { ref } from 'vue'

interface RowVO {
  id: number
  name: string
  nickname: string
  role: string
  sex: string
  sex2: string[]
  num1: number
  age: number
  address: string
  date12: string
  date13: string
}

const tableData = ref<RowVO[]>([
  { id: 10001, name: 'Test1', nickname: 'T1', role: 'Develop', sex: '0', sex2: ['0'], num1: 40, age: 28, address: 'Shenzhen', date12: '', date13: '' },
  { id: 10002, name: 'Test2', nickname: 'T2', role: 'Designer', sex: '1', sex2: ['0', '1'], num1: 20, age: 22, address: 'Guangzhou', date12: '', date13: '2020-08-20' },
  { id: 10003, name: 'Test3', nickname: 'T3', role: 'Test', sex: '0', sex2: ['1'], num1: 200, age: 32, address: 'Shanghai', date12: '2020-09-10', date13: '' },
  { id: 10004, name: 'Test4', nickname: 'T4', role: 'Designer', sex: '1', sex2: ['1'], num1: 30, age: 23, address: 'Shenzhen', date12: '', date13: '2020-12-04' }
])

const sexGriupList = ref([
  {
    label: '第一分组',
    options: [
      { value: '0', label: '女' }
    ]
  },
  {
    label: '第二分组',
    options: [
      { value: '1', label: '男' }
    ]
  },
  {
    label: '其他',
    options: [
      { value: '2', label: '无' }
    ]
  }
])

const formatGroupSex = (value: string) => {
  let label = ''
  sexGriupList.value.find(group => {
    return group.options.find(item => {
      if (item.value === value) {
        label = item.label
        return true
      }
      return false
    })
  })
  return label
}
</script>
